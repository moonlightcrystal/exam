#include <unistd.h>

void solve(char *s1, char *s2)
{
	while(*s2)
	{
		if(*s1 && *s1 == *s2)
			s1++;
		s2++;
	}
	if(*s1 == '\0')
		write(1, "1", 1);
	else
		write(1, "0", 1);
}

int main(int argc, char **argv)
{
	if(argc == 3)
	{
		solve(argv[1], argv[2]);
	}
	write(1, "\n", 1);
	return(0);
}
