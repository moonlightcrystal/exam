#include <unistd.h>

int main(int argc, char **argv)
{
	int i;
	int sp;

	sp = 0;
	i = 0; 
	if(argc == 2)
	{
		while(*argv[1] && (*argv[1] == ' ' || *argv[1] == '\t'))
			argv[1]++;
		while(*argv[1])
		{
			if(*argv[1] == ' ' || *argv[1] == '\t')
			   sp = 1;
			if(*argv[1] != ' ' &&  *argv[1] != '\t')
				{
					if(sp)
						 write(1, " ", 1);
					sp = 0;
					write(1, argv[1], 1);
				}
			argv[1]++;
		}
	}
	write(1, "\n", 1);
	return(0);
}
