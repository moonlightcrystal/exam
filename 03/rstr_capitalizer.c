#include <unistd.h>

void rstr(char *s)
{
	while(*s)
	{
		if(*s >= 'A' && *s <= 'Z')
			*s += 32;
		if((*s >= 'a' && *s <= 'z') && (*(s + 1) == ' ' || *(s + 1) == '\t' || *(s + 1) == '\0'))
			*s -= 32;
		write(1, s++, 1);
	}
}

int main(int argc, char **argv)
{
	int i;

	if(argc != 1)
	{
		i = 1;
		while(argc > i)
		{
			rstr(argv[i]);
			write(1, "\n", 1);
			i++;
		}
	}
	return(0);
}
