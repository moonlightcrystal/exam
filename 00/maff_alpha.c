#include <unistd.h>

int main(void)
{
	char a;

	a = 'a';
	while (a != 'z' + 1)
	{
		if(a %2 == 0)
		{
			write(1, &a, 1);
			a += 32;
		}
		else
		{
			write(1, &a, 1);
			a = a - 32;
		}
		a++;
	}
	write(1, "\n", 1);
	return (0);
}
